struct Cube{
    // Cube Position
    float Px;
    float Py;
    float Pz;
    // Cube Velocity
    float Vx;
    float Vy;
    float Vz;
    // Cube Rotation
    float Ax;
    float Ay;
    float Az;
    // Cube Radius (Size)
    float Radius;
    // Texture Array
    unsigned char *texture;
};
